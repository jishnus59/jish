
#' Power Function
#' 
#' Takes numeric in puts x & y
#' @author  Jishnu
#' @param x numeric variable
#' @param y numeric variable
#' @return x^y 
#' @example example.R
#' @export
Power<- function(x,y){
  return(x^y)
}
#' Squire Function
#' 
#' Takes numeric in puts x 
#' @author  Jishnu
#' @param x numeric variable
#' @return x^2 
#' @example power_example.R
#' @export
Squire<-function(x){
  return(x^2)
